
/** This program demonstrates how to use the String class's length method
 * 
 * @author Ryan Hickman
 *
 */

public class StringLength 
{
	public static void main (String [] args)
	{
		
		String name = "Herman";	// Assigns the name "Herman" to the String variable name
		int stringSize;			// Creates the stringSize variable
		
		stringSize = name.length();	// Calculates the name variable's length
		
		// Displays the message and the character length of Herman
		System.out.println(name + " has " + stringSize + " characters");
		
		
		
		
	}

}
